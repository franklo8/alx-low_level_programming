#include "3-calc.h"
/**
 * op_add - adds 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: 0.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts 2 numbers from each other
 * @a: first number
 * @b: second number
 *
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: 0.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Division of 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Finds the remainder of the division
 * @a: first number
 * @b: second number
 *
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
